public class Calculator {
    
    public double getPenjumlahan(int a, int b){
        double hasil = a + b;
        return hasil;   
    }
    
    public int getPerkalian(int c, int d){
        int perkalian = c * d;
        return perkalian;
    }
    
    public int getPengurangan(int e, int f){
        int pengurangan = e - f;
        return pengurangan;
    }
    
    public int getPembagian(int g, int h){
        int pembagian = g/h;
        return pembagian;
    }
}
